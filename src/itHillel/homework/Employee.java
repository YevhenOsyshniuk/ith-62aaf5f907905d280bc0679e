package itHillel.homework;

class Employee {
    private String name;
    private String surname;
    private String jobTitle;
    private String email;
    private int phoneNumber;
    private int age;

    public Employee(String name, String surname, String jobTitle, String email, int phoneNumber, int age) {
        this.name = name;
        this.surname = surname;
        this.jobTitle = jobTitle;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.age = age;
    }

}
